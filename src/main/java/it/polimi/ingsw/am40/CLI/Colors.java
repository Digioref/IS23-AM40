package it.polimi.ingsw.am40.CLI;

/**
 * Color class with the colors used for the CLI
 */
public class Colors {

    /**
     * @return reset color
     */
    public String rst() {
        return "\u001B[0m";
    }

    /**
     * @return black color
     */
    public String black() {
        return "\u001B[30m";
    }

    /**
     * @return red color
     */
    public String red() {
        return "\u001B[31m";
    }

    /**
     * @return green color
     */
    public String green() {
        return "\u001B[32m";
    }

    /**
     * @return yellow color
     */
    public String yellow() {
        return "\u001B[33m";
    }

    /**
     * @return blue color
     */
    public String blue() {
        return "\u001B[34m";
    }


    /**
     * @return cyan color
     */
    public String cyan() {
        return "\u001B[36m";
    }

    /**
     * @return white color
     */
    public String white() {
        return "\u001B[37m";
    }

    /**
     * @return black background color
     */
    public String blackBg() {
        return "\u001B[40m";
    }

    /**
     * @return green background color
     */
    public String greenBg() {
        return "\u001B[42m";
    }

    /**
     * @return yellow background color
     */
    public String yellowBg() {
        return "\u001B[43m";
    }

    /**
     * @return blue background color
     */
    public String blueBg() {
        return "\u001B[44m";
    }

    /**
     * @return purple background color
     */
    public String purpleBg() {
        return "\u001B[45m";
    }

    /**
     * @return cyan background color
     */
    public String cyanBg() {
        return "\u001B[46m";
    }

    /**
     * @return white background color
     */
    public String whiteBg() {
        return "\u001B[47m";
    }
}
