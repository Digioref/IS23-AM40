package it.polimi.ingsw.am40.Model;

/**
 * An enumeration of the colours of the tiles
 */
public enum TileColor {
    /**
     * Green color of the tiles
     */
    GREEN,
    /**
     * White color of the tiles
     */
    WHITE,
    /**
     * Yellow color of the tiles
     */
    YELLOW,
    /**
     * Blue color of the tiles
     */
    BLUE,
    /**
     * Cyan color of the tiles
     */
    CYAN,
    /**
     * Violet color of the tiles
     */
    VIOLET,
    /**
     * It is used to define a "blank" tile, so it represents an empty position in the board and in the bookshelf
     */
    NOCOLOR;
}

