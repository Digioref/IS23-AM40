/**
 * An enumeration of the types of the tiles
 */
public enum TileType {
    /**
     * Cat type
     */
    CATS,
    /**
     * Book type
     */
    BOOKS,
    /**
     * Game type
     */
    GAMES,
    /**
     * Frame type
     */
    FRAMES,
    /**
     * Trophy type
     */
    TROPHIES,
    /**
     * Plant type
     */
    PLANTS,
    /**
     * It represents a "blank" tile
     */
    EMPTY;
}